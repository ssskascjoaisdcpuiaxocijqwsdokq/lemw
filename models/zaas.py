def normalization(channels: int):
    return nn.InstanceNorm1d(num_features=channels)

# 1D adaptation of MDAF components
class BiasFree_LayerNorm1D(nn.Module):
    def __init__(self, normalized_shape):
        super(BiasFree_LayerNorm1D, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)
        
        assert len(normalized_shape) == 1
        
        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return x / torch.sqrt(sigma + 1e-5) * self.weight

class WithBias_LayerNorm1D(nn.Module):
    def __init__(self, normalized_shape):
        super(WithBias_LayerNorm1D, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)
        
        assert len(normalized_shape) == 1
        
        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.bias = nn.Parameter(torch.zeros(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        mu = x.mean(-1, keepdim=True)
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return (x - mu) / torch.sqrt(sigma + 1e-5) * self.weight + self.bias

class LayerNorm1D(nn.Module):
    def __init__(self, dim, LayerNorm_type):
        super(LayerNorm1D, self).__init__()
        if LayerNorm_type == 'BiasFree':
            self.body = BiasFree_LayerNorm1D(dim)
        else:
            self.body = WithBias_LayerNorm1D(dim)

    def forward(self, x):
        # x: [B, C, L]
        return self.body(x.transpose(-2, -1)).transpose(-2, -1)

class MDAF1D(nn.Module):
    """
    1D adaptation of MDAF for time series data
    Multi-scale feature alignment for frequency and spatial domain features
    """
    def __init__(self, dim, num_heads=8, LayerNorm_type='WithBias'):
        super(MDAF1D, self).__init__()
        self.num_heads = num_heads
        self.dim = dim
        
        self.norm1 = LayerNorm1D(dim, LayerNorm_type)
        self.norm2 = LayerNorm1D(dim, LayerNorm_type)
        self.project_out = nn.Conv1d(dim, dim, kernel_size=1)
        
        # Multi-scale 1D convolutions for first input (frequency domain features)
        self.conv1_1 = nn.Conv1d(dim, dim, kernel_size=7, padding=3, groups=dim)
        self.conv1_2 = nn.Conv1d(dim, dim, kernel_size=11, padding=5, groups=dim)
        self.conv1_3 = nn.Conv1d(dim, dim, kernel_size=21, padding=10, groups=dim)
        
        # Multi-scale 1D convolutions for second input (spatial domain features)
        self.conv2_1 = nn.Conv1d(dim, dim, kernel_size=7, padding=3, groups=dim)
        self.conv2_2 = nn.Conv1d(dim, dim, kernel_size=11, padding=5, groups=dim)
        self.conv2_3 = nn.Conv1d(dim, dim, kernel_size=21, padding=10, groups=dim)

    def forward(self, x1, x2):
        """
        x1: frequency domain features [B, C, L]
        x2: spatial domain features [B, C, L]
        """
        b, c, l = x1.shape
        
        # Normalization
        x1 = self.norm1(x1)
        x2 = self.norm2(x2)
        
        # Multi-scale convolutions for x1 (frequency domain)
        attn_11 = self.conv1_1(x1)
        attn_12 = self.conv1_2(x1)
        attn_13 = self.conv1_3(x1)
        
        # Multi-scale convolutions for x2 (spatial domain)
        attn_21 = self.conv2_1(x2)
        attn_22 = self.conv2_2(x2)
        attn_23 = self.conv2_3(x2)
        
        # Aggregate multi-scale features
        out1 = attn_11 + attn_12 + attn_13
        out2 = attn_21 + attn_22 + attn_23
        
        out1 = self.project_out(out1)
        out2 = self.project_out(out2)
        
        # Simplified cross attention mechanism
        # Ensure num_heads is compatible with channel dimension
        effective_heads = min(self.num_heads, c)
        if c % effective_heads != 0:
            effective_heads = 1  # Fall back to single head if not divisible
        
        head_dim = c // effective_heads
        
        # Create Q, K, V for cross attention
        k1 = out1.view(b, effective_heads, head_dim, l)
        v1 = out1.view(b, effective_heads, head_dim, l)
        k2 = out2.view(b, effective_heads, head_dim, l)
        v2 = out2.view(b, effective_heads, head_dim, l)
        
        # Cross queries: x1 queries x2, x2 queries x1
        q2 = out1.view(b, effective_heads, head_dim, l)  # x1 queries x2
        q1 = out2.view(b, effective_heads, head_dim, l)  # x2 queries x1
        
        # Normalize for stable attention
        q1 = F.normalize(q1, dim=-2)
        q2 = F.normalize(q2, dim=-2)
        k1 = F.normalize(k1, dim=-2)
        k2 = F.normalize(k2, dim=-2)
        
        # Cross attention computation
        attn1 = torch.matmul(q1.transpose(-2, -1), k1)  # [B, effective_heads, L, L]
        attn1 = F.softmax(attn1, dim=-1)
        out3 = torch.matmul(attn1, v1.transpose(-2, -1)).transpose(-2, -1) + q1
        
        attn2 = torch.matmul(q2.transpose(-2, -1), k2)  # [B, effective_heads, L, L]
        attn2 = F.softmax(attn2, dim=-1)
        out4 = torch.matmul(attn2, v2.transpose(-2, -1)).transpose(-2, -1) + q2
        
        # Reshape back
        out3 = out3.view(b, c, l)
        out4 = out4.view(b, c, l)
        
        # Final fusion with residual connections
        out = self.project_out(out3) + self.project_out(out4) + x1 + x2
        
        return out

class moving_avg(nn.Module):
    """
    Moving average block to highlight the trend of time series
    """
    def __init__(self, kernel_size, stride):
        super(moving_avg, self).__init__()
        self.kernel_size = kernel_size
        self.avg = nn.AvgPool1d(kernel_size=kernel_size, stride=stride, padding=0)

    def forward(self, x):
        # padding on the both ends of time series
        # x - B, C, L
        front = x[:, :, 0:1].repeat(1, 1, self.kernel_size - 1-math.floor((self.kernel_size - 1) // 2))
        end = x[:, :, -1:].repeat(1, 1, math.floor((self.kernel_size - 1) // 2))
        x = torch.cat([front, x, end], dim=-1)
        x = self.avg(x)
        return x

class moving_avg_imputation(nn.Module):
    """
    Moving average block modified to ignore zeros in the moving window.
    """
    def __init__(self, kernel_size, stride):
        super(moving_avg_imputation, self).__init__()
        self.kernel_size = kernel_size
        self.stride = stride

    def forward(self, x):
        # Padding on the both ends of time series
        # x - B, C, L
        num_channels = x.shape[1]
        front = x[:, :, 0:1].repeat(1, 1, self.kernel_size - 1 - math.floor((self.kernel_size - 1) // 2))
        end = x[:, :, -1:].repeat(1, 1, math.floor((self.kernel_size - 1) // 2))
        x_padded = torch.cat([front, x, end], dim=-1)

        # Create a mask for non-zero elements
        non_zero_mask = x_padded != 0

        # Calculate sum of non-zero elements in each window
        window_sum = torch.nn.functional.conv1d(x_padded, 
                                                weight=torch.ones((1, num_channels, self.kernel_size)).cuda(),
                                                stride=self.stride)

        # Count non-zero elements in each window
        window_count = torch.nn.functional.conv1d(non_zero_mask.float(), 
                                                  weight=torch.ones((1, num_channels, self.kernel_size)).cuda(),
                                                  stride=self.stride)

        # Avoid division by zero; set count to 1 where there are no non-zero elements
        window_count = torch.clamp(window_count, min=1)

        # Compute the moving average
        moving_avg = window_sum / window_count
        return moving_avg

class series_decomp(nn.Module):
    """
    Series decomposition block
    """
    def __init__(self, kernel_size=24, stride=1, imputation=False):
        super(series_decomp, self).__init__()
        self.kernel_size = kernel_size
        self.stride = stride
        self.moving_avg = moving_avg(kernel_size, stride=stride) if not imputation else moving_avg_imputation(self.kernel_size, self.stride)

    def forward(self, x):
        moving_mean = self.moving_avg(x) 
        res = x - moving_mean
        return res, moving_mean

class EnhancedAdpWaveletBlock(nn.Module):
    """
    Enhanced Adaptive Wavelet Block with MDAF feature alignment
    """
    def __init__(self, configs, input_size):
        super(EnhancedAdpWaveletBlock, self).__init__()
        self.regu_details = configs.regu_details
        self.regu_approx = configs.regu_approx
        if self.regu_approx + self.regu_details > 0.0:
            self.loss_details = nn.SmoothL1Loss()

        self.wavelet = LiftingScheme(configs.enc_in, k_size=configs.lifting_kernel_size, input_size=input_size)
        self.norm_x = normalization(configs.enc_in)
        self.norm_d = normalization(configs.enc_in)
        
        # MDAF for feature alignment between approximation and detail coefficients
        self.mdaf = MDAF1D(configs.enc_in, num_heads=8)
        
        # Feature enhancement layers
        self.feature_enhance = nn.Sequential(
            nn.Conv1d(configs.enc_in, configs.enc_in, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.Conv1d(configs.enc_in, configs.enc_in, kernel_size=1)
        )

    def forward(self, x):
        # Wavelet decomposition
        (c, d) = self.wavelet(x)
        
        # Apply MDAF for feature alignment between approximation and detail coefficients
        # c: approximation coefficients (frequency domain features)
        # d: detail coefficients (spatial domain features)
        aligned_features = self.mdaf(c, d)
        
        # Enhance the approximation coefficients with aligned features
        c_enhanced = c + 0.1 * aligned_features
        
        # Apply feature enhancement
        c_enhanced = self.feature_enhance(c_enhanced)
        
        x = c_enhanced

        r = None
        if(self.regu_approx + self.regu_details != 0.0):
            if self.regu_details:
                rd = self.regu_details * d.abs().mean()
            if self.regu_approx:
                rc = self.regu_approx * torch.dist(c.mean(), x.mean(), p=2)
            if self.regu_approx == 0.0:
                r = rd
            elif self.regu_details == 0.0:
                r = rc
            else:
                r = rd + rc

        x = self.norm_x(x)
        d = self.norm_d(d)
        
        return x, r, d

class InverseAdpWaveletBlock(nn.Module):
    def __init__(self, configs, input_size):
        super(InverseAdpWaveletBlock, self).__init__()
        self.inverse_wavelet = InverseLiftingScheme(configs.enc_in, input_size=input_size, kernel_size=configs.lifting_kernel_size)

    def forward(self, c, d):
        reconstructed = self.inverse_wavelet(c, d)
        return reconstructed

class ClusteredLinear(nn.Module):
    def __init__(self, n_clusters, enc_in, seq_len, pred_len):
        super().__init__()
        self.n_clusters = n_clusters
        self.enc_in = enc_in
        
        # Define a linear layer for each cluster using ModuleDict
        self.linear_layers = nn.ModuleDict({
            str(cluster_id): nn.Linear(seq_len, pred_len) for cluster_id in range(n_clusters)
        })
        
    def forward(self, x, clusters):
        output = []
        assert self.enc_in == len(clusters)
        for channel in range(self.enc_in):
            cluster_id = str(clusters[channel].item())
            channel_data = x[:, channel, :].unsqueeze(1)  # Reshape to keep the channel dimension
            transformed_channel = self.linear_layers[cluster_id](channel_data)
            output.append(transformed_channel)
        output = torch.concat(output, dim=1)
        
        return output
    
class Model(nn.Module):
    """
    ZAAS: AdaWaveNet with MDAF Multi-scale Feature Alignment
    """

    def __init__(self, configs):
        super(Model, self).__init__()
        self.task_name = configs.task_name
        self.seq_len = configs.seq_len
        if self.task_name == 'super_resolution':
            self.seq_len = self.seq_len // configs.sr_ratio
        self.pred_len = configs.pred_len
        self.output_attention = configs.output_attention
        self.kmeans = KMeans(n_clusters=configs.n_clusters)
        self.series_decomp = series_decomp(imputation = self.task_name=='imputation')
        self.rev_seasonal = RevIN(configs.enc_in)
        self.rev_trend = RevIN(configs.enc_in)
        self.trend_linear = ClusteredLinear(configs.n_clusters, configs.enc_in, self.seq_len, configs.pred_len)
        
        # Embedding
        self.enc_embedding = DataEmbedding_inverted(self.seq_len // (2 ** configs.lifting_levels), configs.d_model, configs.embed, configs.freq,
                                                    configs.dropout)
        
         # Construct the levels recursively (encoder) - using Enhanced blocks with MDAF
        self.encoder_levels = nn.ModuleList()
        self.linear_levels = nn.ModuleList()
        self.coef_linear_levels = nn.ModuleList()
        self.coef_dec_levels = nn.ModuleList()
        in_planes = configs.enc_in
        input_size = self.seq_len
        
        if self.task_name == "super_resolution":
            expand_ratio = configs.sr_ratio
        else:
            expand_ratio = 1
        
        for i in range(configs.lifting_levels):
            self.encoder_levels.add_module(
                'encoder_level_'+str(i),
                EnhancedAdpWaveletBlock(configs, input_size)  # Use enhanced block with MDAF
            )
            in_planes *= 1
            input_size = input_size // 2 
            self.linear_levels.add_module(
                'linear_level_'+str(i),
                nn.Sequential(
                    nn.Linear(input_size, input_size * expand_ratio),
                )
            )
            self.coef_linear_levels.add_module(
                'linear_level_'+str(i),
                nn.Sequential(
                    nn.Linear(input_size, input_size * expand_ratio),
                )
            )
            self.coef_dec_levels.add_module(
                'linear_level_'+str(i),
                nn.Sequential(
                    nn.Linear(input_size, input_size * expand_ratio),
                )
            )

        self.input_size = input_size
        
        # Construct the levels recursively (decoder)
        self.decoder_levels = nn.ModuleList()

        for i in range(configs.lifting_levels-1, -1, -1):
            self.decoder_levels.add_module(
                'decoder_level_'+str(i),
                InverseAdpWaveletBlock(configs, input_size=input_size)
            )
            in_planes //= 1
            input_size *= 2
        
        if self.task_name == "super_resolution":
            self.lowrank_projection = nn.Linear(configs.d_model, self.pred_len // (2 ** configs.lifting_levels), bias=True)
        else:
            self.lowrank_projection = nn.Linear(configs.d_model, self.seq_len // (2 ** configs.lifting_levels), bias=True)

        # Encoder
        self.encoder = Encoder(
            [
                EncoderLayer(
                    AttentionLayer(
                        FullAttention(False, configs.factor, attention_dropout=configs.dropout,
                                      output_attention=configs.output_attention), configs.d_model, configs.n_heads),
                    configs.d_model,
                    configs.d_ff,
                    dropout=configs.dropout,
                    activation=configs.activation
                ) for l in range(configs.e_layers)
            ],
            norm_layer=torch.nn.LayerNorm(configs.d_model)
        )
        # Decoder
        if self.task_name == 'long_term_forecast' or self.task_name == 'short_term_forecast':
            self.projection = nn.Linear(self.seq_len, configs.pred_len, bias=True)
        if self.task_name == 'imputation':
            self.projection = nn.Linear(self.seq_len, self.seq_len, bias=True)
        if self.task_name == 'anomaly_detection':
            self.projection = nn.Linear(self.seq_len, self.seq_len, bias=True)
        if self.task_name == 'super_resolution':
            self.projection = nn.Linear(configs.pred_len, configs.pred_len, bias=True)

        self.register_buffer('clusters', None)

    def forecast(self, x_enc, x_mark_enc, x_dec, x_mark_dec, clusters):
        x, moving_mean = self.series_decomp(x_enc.permute(0,2,1))
        moving_mean = moving_mean.permute(0,2,1)
        # Normalization from Non-stationary Transformer
        x_enc = x.permute(0,2,1)
        means = x_enc.mean(1, keepdim=True).detach()
        x_enc = x_enc - means
        stdev = torch.sqrt(torch.var(x_enc, dim=1, keepdim=True, unbiased=False) + 1e-5)
        x_enc = x_enc / stdev  # Avoid in-place operation
        _, _, N = x_enc.shape

        moving_mean = self.rev_trend(moving_mean, 'norm')

        x_enc = x_enc.permute(0,2,1)
        encoded_coefficients = []
        x_embedding_levels = []
        coef_embedding_levels = []
        # Encoding with MDAF-enhanced wavelet blocks
        for l, l_linear, c_linear in zip(self.encoder_levels, self.linear_levels, self.coef_linear_levels):
            x_enc, r, details = l(x_enc)
            encoded_coefficients.append(details)
            coef_embedding_levels.append(c_linear(details))
            x_embedding_levels.append(l_linear(x_enc))
        # Embedding
        x_enc = x_enc.permute(0,2,1)
        enc_out = self.enc_embedding(x_enc, None)
        enc_out, attns = self.encoder(enc_out, attn_mask=None)
        x_dec = self.lowrank_projection(enc_out)
        # Decoding
        for dec, x_emb_level, coef_emb_level, c_linear in zip(self.decoder_levels, x_embedding_levels[::-1], coef_embedding_levels[::-1], self.coef_dec_levels[::-1]):
            details = encoded_coefficients.pop()
            details = coef_emb_level + c_linear(details)
            x_dec = x_dec + x_emb_level
            x_dec = dec(x_dec, details)
        dec_out = self.projection(x_dec).permute(0, 2, 1)[:, :, :N]
        # De-Normalization from Non-stationary Transformer
        dec_out = dec_out * (stdev[:, 0, :].unsqueeze(1).repeat(1, self.pred_len, 1))
        dec_out = dec_out + (means[:, 0, :].unsqueeze(1).repeat(1, self.pred_len, 1))
        
        moving_mean_out = self.trend_linear(moving_mean.permute(0,2,1), self.clusters).permute(0,2,1)
        moving_mean_out = self.rev_trend(moving_mean_out, 'denorm')
        
        dec_out = dec_out + moving_mean_out
        
        return dec_out

